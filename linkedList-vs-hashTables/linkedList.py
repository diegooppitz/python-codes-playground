class Node:
    def __init__(self, data):
        self.data = data
        self.next = None

class LinkedList:
    def __init__(self):
        self.head = None

    def append(self, data):
        new_node = Node(data)
        if self.head is None:
            self.head = new_node
        else:
            current = self.head
            while current.next:
                current = current.next
            current.next = new_node

    def display(self):
        current = self.head
        while current:
            print(current.data, end=" ") 
            current = current.next 

my_list = LinkedList() # Create a new linked list

my_list.append("apple")
my_list.append("banana")
my_list.append("strawberry")
my_list.display()


